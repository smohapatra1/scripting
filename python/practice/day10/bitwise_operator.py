#!/usr/bim/python
#Bitwise operator
var1 = 5
var2 = 6
#AND
print ("AND operator % d " % (int(var1 & var2)))
#OR
print ( "OR operator % d " % int(var1 | var2))
#XOR
print ("XOR operator %d " % int(var1 ^ var2))
#ones Complement
print ("Onces complement %d " % int(~var1))
#Binary Shift left
print ("Shift left %d " %(var1 << 1))
#Binary Shift Right 
print ("Shift right %d " % ( var1 >> 1))
